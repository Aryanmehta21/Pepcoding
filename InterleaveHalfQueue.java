import java.util.*;
import java.util.LinkedList;
public class InterleaveHalfQueue {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        Queue<Integer> q = new LinkedList<>();
        for(int i = 0;i<n;i++){
            int x = sc.nextInt();
            q.add(x);
        }

        
    }
}
